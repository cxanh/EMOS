router.post('/generate/:type', authenticateToken, async (req, res, next) => {
  try {
    const { type } = req.params;
    const { nodeIds } = req.body;

    // 楠岃瘉鎶ヨ〃绫诲瀷
    const validTypes = reportService.getReportTypes();
    if (!validTypes.includes(type)) {
      return res.status(400).json({
        success: false,
        error: {
          code: 'INVALID_TYPE',
          message: `Report type must be one of: ${validTypes.join(', ')}`
        }
      });
    }

    // 楠岃瘉鑺傜偣ID
    if (!nodeIds || !Array.isArray(nodeIds) || nodeIds.length === 0) {
      return res.status(400).json({
        success: false,
        error: {
          code: 'MISSING_FIELDS',
          message: 'Node IDs are required'
        }
      });
    }

    // 鐢熸垚鎶ヨ〃
    const report = await reportService.generatePredefinedReport(
      type,
      nodeIds,
      req.user.user_id
    );

    logger.info(`${type} report generated by user ${req.user.username}: ${report.id}`);

    res.json({
      success: true,
      data: report,
      message: `${type.charAt(0).toUpperCase() + type.slice(1)} report generated successfully`
    });
  } catch (error) {
    next(error);
  }
});

