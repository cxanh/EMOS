router.delete('/:reportId', authenticateToken, async (req, res, next) => {
  try {
    const { reportId } = req.params;

    // 妫€鏌ユ姤琛ㄦ槸鍚﹀瓨鍦?
    const report = await reportService.getReport(reportId);
    if (!report) {
      return res.status(404).json({
        success: false,
        error: {
          code: 'REPORT_NOT_FOUND',
          message: 'Report not found'
        }
      });
    }

    // 鍙湁绠＄悊鍛樻垨鎶ヨ〃鍒涘缓鑰呭彲浠ュ垹闄?
    if (req.user.role !== 'admin' && report.createdBy !== req.user.user_id) {
      return res.status(403).json({
        success: false,
        error: {
          code: 'FORBIDDEN',
          message: 'You can only delete your own reports'
        }
      });
    }

    await reportService.deleteReport(reportId);

    logger.info(`Report deleted by user ${req.user.username}: ${reportId}`);

    res.json({
      success: true,
      message: 'Report deleted successfully'
    });
  } catch (error) {
    next(error);
  }
});

