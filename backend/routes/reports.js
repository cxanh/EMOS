// 鐢熸垚鑷畾涔夋姤琛?
router.post('/generate', authenticateToken, async (req, res, next) => {
  try {
    const { nodeIds, startTime, endTime, metrics } = req.body;

    // 楠岃瘉蹇呭～瀛楁
    if (!nodeIds || !Array.isArray(nodeIds) || nodeIds.length === 0) {
      return res.status(400).json({
        success: false,
        error: {
          code: 'MISSING_FIELDS',
          message: 'Node IDs are required'
        }
      });
    }

    if (!startTime || !endTime) {
      return res.status(400).json({
        success: false,
        error: {
          code: 'MISSING_FIELDS',
          message: 'Start time and end time are required'
        }
      });
    }

    // 楠岃瘉鏃堕棿鑼冨洿
    const start = new Date(startTime);
    const end = new Date(endTime);
    
    if (isNaN(start.getTime()) || isNaN(end.getTime())) {
      return res.status(400).json({
        success: false,
        error: {
          code: 'INVALID_TIME',
          message: 'Invalid time format'
        }
      });
    }

    if (start >= end) {
      return res.status(400).json({
        success: false,
        error: {
          code: 'INVALID_TIME_RANGE',
          message: 'Start time must be before end time'
        }
      });
    }

    // 鐢熸垚鎶ヨ〃
    const report = await reportService.generateReport({
      type: 'custom',
      nodeIds,
      startTime,
      endTime,
      metrics,
      userId: req.user.user_id
    });

    logger.info(`Custom report generated by user ${req.user.username}: ${report.id}`);

    res.json({
      success: true,
      data: report,
      message: 'Report generated successfully'
    });
  } catch (error) {
    next(error);
  }
});

